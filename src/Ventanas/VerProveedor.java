        private void mostar_datos(DefaultTableModel tabla, int n, int m) {
        DefaultTableModel ModeloTabla = (DefaultTableModel) TablaInfo.getModel();
        int row = 0, column = 0;
        NodoSegundario subnodo = proveedores.getInicio();
        while (subnodo != null) {
            add_proveedor info = (add_proveedor) subnodo.getInfo();
            tabla.setValueAt(info.getNom(), row, column);
            column++;
            tabla.setValueAt(info.getTelefono(), row, column);
            column++;
            tabla.setValueAt(info.getCorreo(), row, column);
            column++;
            row++;
            column = 0;
            subnodo = subnodo.getSiguiente();
        }
        TablaInfo.setModel(tabla);
    }

    private void modelo() {
        DefaultTableModel ModeloTabla = (DefaultTableModel) TablaInfo.getModel();
        int contR = 0, contC = 3;
        NodoSegundario subnodo = proveedores.getInicio();
        while (subnodo != null) {
            contR++;
            subnodo = subnodo.getSiguiente();
        }
        ModeloTabla.setRowCount(contR);
        ModeloTabla.setColumnCount(contC);
        mostar_datos(ModeloTabla, contR, contC);
    }
